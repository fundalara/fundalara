package comun;

public class Inning {
	
	private int valor;
	private int indice;
	
	public Inning(int valor, int indice) {
		this.valor = valor;
		this.indice = indice;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	public int getIndice() {
		return indice;
	}
	public void setIndice(int indice) {
		this.indice = indice;
	}
	
	
	

}
